    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Fornecedor fornecedor = new Fornecedor();
        
        fornecedor.setId(idFornecedor);
        fornecedor.setNome(jTextFieldNome.getText());
        fornecedor.setEmail(jTextFieldEmail.getText());
        fornecedor.setTelefone(jFormattedTextFieldTelefone.getText());
        fornecedor.setCnpj(jFormattedTextFieldCnpj.getText());
        
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        if (fornecedorDAO.validarCampos(fornecedor)) {
            if (idFornecedor == 0) {                    // ID ser zero significa que é insert
                fornecedorDAO.insert(fornecedor);
            } else {
                fornecedorDAO.update(fornecedor);
            }
        }
        jTextFieldNome.setText("");
        jTextFieldEmail.setText("");
        jFormattedTextFieldTelefone.setText("");
        jFormattedTextFieldCnpj.setText("");
            
        jTabbedPane.setSelectedIndex(0);
            
        idFornecedor = 0;
    }//GEN-LAST:event_jButtonSalvarActionPerformed

