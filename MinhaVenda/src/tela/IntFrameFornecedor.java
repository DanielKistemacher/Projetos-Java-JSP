    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        if (jTableFornecedores.getSelectedRow() == -1) {
            Prompts.promptAlerta("Selecione um fornecedor da tabela.");
        } else {

            String idTabela = String.valueOf(jTableFornecedores.getValueAt(jTableFornecedores.getSelectedRow(), 0));

            idFornecedor = Integer.parseInt(idTabela);

            Fornecedor fornecedor = new FornecedorDAO().consultarId(idFornecedor);

            if (fornecedor != null) {
                jTabbedPane.setSelectedIndex(1);

                jTextFieldNome.setText(fornecedor.getNome());
                jTextFieldEmail.setText(fornecedor.getEmail());
                jFormattedTextFieldTelefone.setText(fornecedor.getTelefone());
                jFormattedTextFieldCnpj.setText(fornecedor.getCnpj());

                jTextFieldNome.requestFocus();
            } else {
                Prompts.promptErro("Não foi possível localizar o ID do fornecedor.");
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTableFornecedores.getSelectedRow() == -1) {
            Prompts.promptAlerta("Selecione um fornecedor da tabela.");
        } else {
        
            String idTabela = String.valueOf(jTableFornecedores.getValueAt(jTableFornecedores.getSelectedRow(), 0));

            idFornecedor = Integer.parseInt(idTabela);
            
            FornecedorDAO fornecedorDAO = new FornecedorDAO();
            Fornecedor fornecedor = new FornecedorDAO().consultarId(idFornecedor);

            boolean opcao = Prompts.promptConfirmar("Tem certeza que deseja excluir o fornecedor: " + fornecedor.getNome() + " ?");
            if (opcao == true) {
                fornecedorDAO.delete(fornecedor.getId());
            }
        }
        idFornecedor = 0;
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jFormattedTextFieldCnpjFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextFieldCnpjFocusLost
        if (!Validacao.validarCNPJ(Formatacao.removerFormatacao(jFormattedTextFieldCnpj.getText()))) {
            jFormattedTextFieldCnpj.setBackground(Color.red);
        } else {
            jFormattedTextFieldCnpj.setBackground(Color.green);
        }
    }//GEN-LAST:event_jFormattedTextFieldCnpjFocusLost

