    //M√©todo que ir√° remover acentos e caracteres especiais da palavra digitada
    public static String corrigirString(String texto){
        String nfdNormalizedString = Normalizer.normalize(texto, Normalizer.Form.NFD);
        Pattern pattern = Pattern.compile("\\p{InCombiningDiacriticalMarks}+");
        return pattern.matcher(nfdNormalizedString).replaceAll("");
    }
    
    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        if (palavraSecreta.getText().isEmpty()){ //Ver se existe texto digitado
            JOptionPane.showMessageDialog(null, "Informe uma palavra!");
        } else {
            String texto = palavraSecreta.getText();
            palavras.add(corrigirString(texto)); //Adiciona a palabra digitada no Arraylist ap√≥s utilizar o m√©todo para corrigir a palavra
            JOptionPane.showMessageDialog(null, "Palavra gravada!");
            palavraSecreta.setText("");
            palavraSecreta.requestFocus(); 
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoComecarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoComecarActionPerformed
        int t = palavras.size(); //pegar o tamanho do ArrayList
        Random random = new Random();
        int numeroRandom = random.nextInt(t); //gerando n√∫mero random
        palavraSorteada.setText(palavras.get(numeroRandom).toUpperCase()); //usando o n√∫mero random para pegar a posi√ß√£o (palavra) no Arraylist
        
        char[] palavraEscolhida = palavraSorteada.getText().toCharArray();
        String secreto = "";
        for(int i = 0; i < palavraEscolhida.length; i++){        
            secreto = secreto + "-";
        }
        palavraAdivinhar.setText(secreto);
        letraChute.requestFocus();    
    }//GEN-LAST:event_botaoComecarActionPerformed

    private void botaoChutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoChutarActionPerformed
        if (letraChute.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite uma Letra!");
            letraChute.requestFocus();
        } else {
            char[] palavraQuebrada = palavraSorteada.getText().toUpperCase().toCharArray();
            String secreto = palavraAdivinhar.getText();
            StringBuilder adivinha = new StringBuilder(secreto);
            
            char letra = letraChute.getText().toUpperCase().charAt(0);
                
            boolean encontrou = false;
            for(int i = 0; i < palavraQuebrada.length; i++){
                if (palavraQuebrada[i] == letra) {
                    adivinha.setCharAt(i, letra);
                    JOptionPane.showMessageDialog(null, "Acertou");
                    palavraAdivinhar.setText(adivinha.toString());  
                    encontrou = true;                    
                }
            }
        
            int qtdVidas = Integer.parseInt(vidas.getText());            
            if (encontrou == false) {
                qtdVidas--;
                vidas.setText(Integer.toString(qtdVidas));
                JOptionPane.showMessageDialog(null, "Errou");                
            }
            
            if (palavraSorteada.getText().equals(palavraAdivinhar.getText())) {
                JOptionPane.showMessageDialog(null, "Voc√™ acertou toda a palavra!");  
                palavraAdivinhar.setText("?????");
                vidas.setText("5");
            }
            
            if (qtdVidas == 0) {
                JOptionPane.showMessageDialog(null, "Voc√™ perdeu!");
                palavraSorteada.setVisible(true);
                jLabel6.setVisible(true);
                palavraAdivinhar.setText("Univates");                
                vidas.setText("5");
                int opcao = JOptionPane.showConfirmDialog(null, "Jogar novamente ?", "Voc√™ perdeu",JOptionPane.YES_NO_OPTION);
                if (opcao == JOptionPane.NO_OPTION){
                    System.exit(0);
                } else if (opcao == JOptionPane.YES_OPTION){
                    palavraSorteada.setVisible(false);
                    jLabel6.setVisible(false);
                }
            }       
        letraChute.setText("");
        letraChute.requestFocus();
        }
    }//GEN-LAST:event_botaoChutarActionPerformed

